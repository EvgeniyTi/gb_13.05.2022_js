'use strict'

function sum(a, b) {
    return a + b;
}

function minus(a, b) {
    return a - b;
}

function mul(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}


let resultSum = sum(4, 3);
console.log(resultSum);

let resultMinus = minus(4, 3);
console.log(resultMinus);

let resultMul = mul(4, 3);
console.log(resultMul);

let resultDivide = divide(4, 3);
console.log(resultDivide);